<h1>HOTEL STREYM</h1>
Hotel Streym is located in the centre of Tórshavn, while still featuring a seaside view.
<br><br>
The hotel aims to offer the freshest hotel experience possible. Their “no frills philosophy” means: everything you need, without all that extra stuff that you never use but still end up paying for. Next to the breakfast restaurant you’ll find some comfy couches where you can relax in front of the flatscreen-tv, with a complementary cup of coffee.
<br><br>
However, “no frills” doesn’t mean no contact to the world. At Hotel Streym they know how important staying in touch is, whether it’s for work or personal use, the broadband internet connection is always at your disposal, for free.
