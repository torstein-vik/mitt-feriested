<h1>HOTEL TÓRSHAVN</h1>
This 3-star hotel is perfectly located in the centre of Tórshavn with a stunning view of the harbour and the Town Hall Square. Only a few mintues walk to the city centre. It includes a restaurant, café, cocktail bar and 6 excellent meeting rooms, suited for both large and small groups.
<br><br>
Whether you are 10, 30 or 60 people, the hotel can provide all the conference equipment needed. The conference rooms are equipped with projector, overhead, whiteboard, flip over, free wireless network and broadband.
<br><br>
There are 43 rooms on the hotel with a 73 bed capacity. The restaurant “Hvonn Brasserie” is on the first floor, and on the ground floor is a café & bar.
