<h1>HOTEL VÁGAR</h1>
Hotel Vágar is a business- and tourist hotel which has 22 double rooms, 2 suites, and 3 meeting rooms, all fully equipped with AV, restaurant and bar. The 62ºN airport hotel – earlier named Hotel Vágar – is only 2 minutes’ walk from Vágar Airport.
<br><br>
Enjoy the silence at the hotel or a fresh walk to the village of Sørvágur, only a kilometre down the road. There you can take in the views of the the harbour and sea, or even take a boat trip to the fantastic island of Mykines.
