    <p>
        The remote location of the 18 islands constituting the Faroe Islands functions virtually as a
        magnet for birds migrating over the North Atlantic Ocean.
    </p>
    <p>
        The Faroes are exceptionally located for getting birds from the east, but several North American
        species have also been recorded at the Faroes. There is just a handful of local birders, so the
        chances of finding rare birds by yourself are very good.
    </p>
    <p>
        Rare birds can turn up everywhere, but there is a tendency that birds concentrate at the northern,
        southern, eastern and westernmost points, depending on the dominant winds. This is due to the
        fact that these places are the first bits of land the birds see. This makes islands such as
        Svínoy, Fugloy, Mykines and Suðuroy some of the best hotspots for migrants. It is possible to
        rent houses at most locations at any season through local tourist agencies.
    </p>
    <p>
        The landscape is both helpful and challenging. Due to the lack of trees birds will often end up
        in gardens in villages or seek shelter in the plantations. At times these bird magnets can be
        full of birds and a joy for birdwatchers. In the autumn of 2009 one single garden contained two
        Arctic Warblers, two Yellow-browed Warblers, a Barred Warbler, two Chiffchaffs, a Song Thrush
        and a Ring Ouzel. So when it comes to birds that are attracted to gardens it is fairly easy to
        find them. Other birds like pipits and buntings, which prefer grassy fields, have so much suitable
        habitat on the islands that finding them can be a hard task.
    </p>
    <p>
        Just about 300 different bird species have been recorded at the Faroes, but only about 100 are
        regular migrants or breeding birds. This means that about 200 species are rare migrants and new
        birds are added to the national list every year.
    </p>
